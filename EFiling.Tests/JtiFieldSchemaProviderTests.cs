    [Fact]
    public void Step42_KnownCategoryCodes_NoOverlapBetweenPolicies()
    {
        // Each numeric per-court code should map to AT MOST ONE JCCC policy entry.
        // Overlap would indicate a data error — the resolver returns the first match
        // and downstream consumers would see ambiguous behavior.
        var schema = JtiFieldSchemaProvider.GetCaseCategoryPolicy();
        var codeToCategory = new Dictionary<string, string>(StringComparer.OrdinalIgnoreCase);

        foreach (var (catKey, policy) in schema.Policies)
        {
            if (policy.KnownCategoryCodes is not { Count: > 0 } codes)
                continue;

            foreach (var code in codes)
            {
                Assert.False(codeToCategory.ContainsKey(code),
                    $"Numeric category code '{code}' appears in BOTH '{codeToCategory.GetValueOrDefault(code)}' and '{catKey}'. " +
                    $"Each numeric code must map to exactly one JCCC policy.");
                codeToCategory[code] = catKey;
            }
        }
    }

