    // ───────────────────────────────────────────────────────────────────
    // Schema ⇄ baseline cross-reference
    //
    // Partner to TierA_CoverageMatrixTests.Subsequent_TagTypeCounts_MatchCatalog_*,
    // which locks counts against the catalog. That test alone lets a drift slip where
    // a new baseline tag is added to ExpectedTagCounts + catalog but the schema file
    // is forgotten — the mapper's DetermineTagValue would then silently emit "1"
    // (see anchor in MetadataValueMapperTests). This cross-reference asserts every
    // tagType present in any canonical subsequent baseline is ALSO declared in
    // JtiTagSchema.json, so schema drift is caught at the schema layer too.
    //
    // The companion test `Initiation_Baselines_UseTypedExtensions_NotTagTypePattern`
    // locks the architectural finding from 2026-04-25 P0 preflight: initial filings
    // use TYPED `CaseAugmentationExt` children for case-level metadata, while
    // subsequent filings use the GENERIC tag-triple pattern. JtiTagSchema.json is
    // therefore subsequent-filing-specific by design — the initial-filing wire path
    // exercises a different code path through the builder (typed-element emission).
    // ───────────────────────────────────────────────────────────────────

    [Fact]
    public void TagSchema_DeclaresEveryTagType_ObservedInSubsequentBaselines()
    {
        // Walk every subsequent baseline and collect the distinct tagType values that appear
        // on the wire. Assert each is a declared entry in JtiTagSchema.json.
        var schema = JtiFieldSchemaProvider.GetTagSchema();
        Assert.NotNull(schema);

        var observedTags = new HashSet<string>(StringComparer.Ordinal);
        foreach (var scenario in CanonicalScenarios.ByFilingType(FilingType.Subsequent))
        {
            var doc = SampleLoader.LoadXDocument(scenario);
            foreach (var tagEl in doc.Descendants().Where(e => e.Name.LocalName == "tagType"))
            {
                var value = tagEl.Value?.Trim();
                if (!string.IsNullOrEmpty(value))
                    observedTags.Add(value!);
            }
        }

        // Sanity: we expect to observe at least the 4 evidence-backed tags per §4.7.
        Assert.True(observedTags.Count >= 4,
            $"Baseline walk produced only {observedTags.Count} distinct tagTypes — expected ≥ 4 " +
            $"(E_SERVICE, FEE_EXEMPTION, EFSP_FIRST_APPEARANCE_PAID, EFSP_EMAIL). The sample loader " +
            $"or canonical registry may be broken.");

        // The actual cross-reference assertion.
        var missing = observedTags.Where(t => !schema.Tags.ContainsKey(t)).ToList();
        Assert.True(missing.Count == 0,
            "Schema drift detected: the following tagType values appear in baseline samples but " +
            "are NOT declared in JtiTagSchema.json. Add a schema entry (with tagValueKind + " +
            "evidence metadata) so the mapper and builder have an authoritative source:\n  - " +
            string.Join("\n  - ", missing));
    }

    [Fact]
    public void Initiation_Baselines_UseTypedExtensions_NotTagTypePattern()
    {
        // Architectural contract (discovered 2026-04-25 P0 preflight, locked here as
        // regression bar): the metadata-bearing wire structures differ by filing-type.
        //
        //   Subsequent filings (per-doc metadata):
        //     <DocumentFilingMetaData>
        //       <tagType>FEE_EXEMPTION</tagType>
        //       <tagValue>FEE_WAIVER</tagValue>
        //     </DocumentFilingMetaData>
        //
        //   Initial filings (case-level metadata):
        //     <CaseAugmentationExt>
        //       <p:efmFeeExemptionRequestType>...</p:efmFeeExemptionRequestType>
        //     </CaseAugmentationExt>
        //
        // This is why JtiTagSchema.json is correctly subsequent-filing-specific. The
        // initial-filing path emits typed `CaseAugmentationExt`/`PartyAugmentation` children
        // through a different builder code path. Auditing CC's metadata correctness (P2b)
        // therefore requires a different lens than auditing SF's (P2a).
        //
        // If a future initial-filing baseline starts using <tagType> elements, this test
        // catches it — that would represent a JTI wire-format change requiring schema +
        // builder + mapper updates.
        var initiationTagTypeCount = 0;
        foreach (var scenario in CanonicalScenarios.ByFilingType(FilingType.Initiation))
        {
            var doc = SampleLoader.LoadXDocument(scenario);
            initiationTagTypeCount += doc.Descendants()
                .Count(e => e.Name.LocalName == "tagType");
        }

        Assert.Equal(0, initiationTagTypeCount);
    }

