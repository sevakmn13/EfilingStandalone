    [Fact]
    public void TFourA_AllClassTypes_AllDropdownSources_AreLiveVerifiedNames()
    {
        // Defense-in-depth test: every dropdown field in EVERY classType / variant
        // must use a source name that we've live-verified against Madera (or
        // the 'dynamic' sentinel meaning "host page supplies the codelist name
        // at runtime"). This catches a future schema edit that introduces a new
        // dropdown with an unverified source name — which would silently fall
        // back to [] from Madera's codelist API and produce an empty dropdown.
        //
        // ORIGINALLY scoped to just the contact composite — but the address
        // composite had the SAME 2 source-name bugs (STATE/COUNTRY) and the
        // contact-only test missed them. Generalized 2026-05-17 to cover all
        // classTypes so any similar slip surfaces immediately.
        //
        // Live verification protocol when adding a new source name:
        //   1. Run: GET http://localhost:58086/CourtFiling/api/codelist?courtId=madera&codeListType={SOURCE}
        //   2. Confirm response is non-empty array
        //   3. Add the source name to `liveVerified` below + cite the verification date
        var liveVerified = new HashSet<string>(StringComparer.Ordinal)
        {
            // Verified 2026-05-17 against Madera staging /api/codelist endpoint:
            "US_STATE",         // 50 entries (was 'STATE' which returned [])
            "ADDRESS_COUNTRY",  // 4814 bytes — canonical (was 'COUNTRY' which returned 797 bytes)
            "ADDRESS_TYPE",     // 675 bytes — Mailing/Work/Home/etc.
            "TELEPHONE_TYPE",   // 5 entries (was 'PHONE_TYPE' which returned [])
            "PARTY_TYPE",       // 357826 bytes — large list of party-role codes (Administrator, Plaintiff, etc.)

            // Sentinel: source 'dynamic' means the host page supplies the codelist
            // name at runtime via spec.dynamicSource (e.g., codeList classType
            // delegates to meta.CodeList from document metadata).
            "dynamic"
        };

        var schema = JtiFieldSchemaProvider.GetClassTypeSchema();
        var failures = new List<string>();
        foreach (var (classTypeName, classType) in schema.ClassTypes)
        {
            foreach (var (variantName, variant) in classType.Variants)
            {
                if (variant.Fields == null) continue;
                foreach (var field in variant.Fields.Where(f => f.Type == "dropdown"))
                {
                    if (string.IsNullOrEmpty(field.Source))
                    {
                        failures.Add($"{classTypeName}.{variantName}.{field.Name}: missing `source`");
                        continue;
                    }
                    if (!liveVerified.Contains(field.Source))
                    {
                        failures.Add($"{classTypeName}.{variantName}.{field.Name}: source=`{field.Source}` NOT in live-verified set");
                    }
                }
            }
        }
        Assert.True(failures.Count == 0,
            $"The following dropdown fields use sources not in the live-verified set:" +
            Environment.NewLine + string.Join(Environment.NewLine, failures) +
            Environment.NewLine + Environment.NewLine +
            $"Live-verified set: [{string.Join(", ", liveVerified)}]" +
            Environment.NewLine +
            "To resolve: GET http://localhost:58086/CourtFiling/api/codelist?courtId=madera&codeListType={SOURCE}, confirm non-empty, add to set.");
    }
